<?php
    // Operador de pós-incremento
    // Em uma expressão retorna o valor da variavel e incrementa depois, ou seja o incremento só vale para expressões futuras
    $a = 5;
    echo $a++;
    echo "<br>";
    echo $a;
    echo "<br>";

    // Operador de pré-incremento
    // Em uma expressão retorna o valor incrementado da variável
    $a = 5;
    echo ++$a;
    echo "<br>";

    // Operador de pós-decremento
    // Em uma expressão retorna o valor da variavel e decrementa depois, ou seja o decremento só vale para expressões futuras
    $a = 5;
    echo $a--;
    echo "<br>";
    echo $a;
    echo "<br>";

    // Operador de pré-decremento
    // Em uma expressão retorna o valor decrementado da variável
    $a = 5;
    echo --$a;
    echo "<br>";